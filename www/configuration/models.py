# -*- coding: utf-8 -*-

from django.db import models

#######################################################################################################################
#######################################################################################################################

#Настройки
class ConfigModel(models.Model):
		
	is_active = models.BooleanField(verbose_name=u'активно', default=True)
	
	def __unicode__(self):
		return u'настройки'
		
	class Meta: 
		verbose_name = u'настройки' 
		verbose_name_plural = u'настройки'

